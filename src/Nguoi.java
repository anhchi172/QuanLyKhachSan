public class Nguoi {
    private String hoTen;
    private int tuoi;
    private int soCMND;
    public Nguoi(String h, int t, int s){
        hoTen = h;
        tuoi = t;
        soCMND = s;
    }
    public String getHoTen(){
        return hoTen;
    }
    public int getTuoi(){
        return tuoi;
    }
    public int getSoCMND(){
        return soCMND;
    }


}
